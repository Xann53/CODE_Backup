public class Conversion
{
  public static void convert(double meter)
  {
      System.out.println("\nThe conversion value of " + meter + " meter/s to centimeter is equal to " + (meter * 100) + " cm.");
      System.out.println("The conversion value of " + meter + " meter/s to millimeter is equal to " + (meter * 1000) + " mm.");
      System.out.println("The conversion value of " + meter + " meter/s to inches is equal to " + (meter * 39.37) + " inches.");
      System.out.println("The conversion value of " + meter + " meter/s to feet is equal to " + (meter * 3.28084) + " ft.");
      System.out.println("The conversion value of " + meter + " meter/s to yard is equal to " + (meter * 1.094) + " yards.");
      System.out.println("The conversion value of " + meter + " meter/s to mile is equal to " + (meter / 1600) + " miles.");
 }

}