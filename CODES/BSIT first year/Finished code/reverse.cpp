#include<iostream>
using namespace std;
int main()
{

	int num1, num2, num3;
	
	cout<<"Enter three(3) numbers:\n\n";
	cout<<"\t";
	cin>>num1;
	cout<<"\t";
	cin>>num2;
	cout<<"\t";
	cin>>num3;
	cout<<"\n\n\n"<<endl;
	
	cout<<"Your numbers forward:\n\n";
	cout<<"\t"<<num1<<"\n";
	cout<<"\t"<<num2<<"\n";
	cout<<"\t"<<num3<<"\n\n";
	
	cout<<"Your numbers reversed:\n\n";
	cout<<"\t"<<num3<<"\n";
	cout<<"\t"<<num2<<"\n";
	cout<<"\t"<<num1<<"\n\n";
	
	return 0;

}

